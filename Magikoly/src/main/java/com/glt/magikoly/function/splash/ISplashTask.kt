interface ISplashTask {

    /**
     * 预加载任务的key
     */
    fun preloadKey(): String

    /**
     * 开启预加载
     */
    fun startPreload()

    /**
     * 判断是否加载完成
     */
    fun isPreloadFinished(): Boolean

    /**
     * 清除预加载
     */
    fun onCancelPreload(taskAllFinished: Boolean)

    /**
     * 每0.5秒回调一次
     */
    fun onTick(currentMills: Long)
}