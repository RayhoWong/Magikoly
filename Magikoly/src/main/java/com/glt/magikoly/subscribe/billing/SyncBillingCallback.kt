class SyncBillingCallback : IBillingCallback {
    override fun onBillingError(productID: String, code: Int) {

    }

    override fun onInitialized() {
    }

    override fun onPurchased(billingOrder: BillingOrder) {
    }

    override fun onSyncBillingOrderFinished(purchaseList: List<BillingOrder>) {
        var isVIP = BillingStatusManager.getInstance().isVIP()
        var isVIPCurrent = false
        for (billingOrder in purchaseList) {
            if (BillingStatusManager.getInstance().isVIPProductId(billingOrder.skuId)) {
                isVIPCurrent = true
                if (isVIP) {
                    return
                } else {
                    BillingStatusManager.getInstance().registerVIP()
                    isVIP = BillingStatusManager.getInstance().isVIP()
                }
            }
        }
        if (!isVIPCurrent && isVIP) {
            BillingStatusManager.getInstance().unregisterVIP()
        }
    }

    override fun onConsumed(id: String) {
    }
}