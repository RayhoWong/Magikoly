public class BillingOrder implements Parcelable {

    public String skuId;

    public String orderId;

    public String purchaseToken;

    public long purchaseTime;

    public int entrance; //表示从哪个入口创建的订单

    public String getSkuId() {
        return skuId;
    }

    public void setSkuId(String skuId) {
        this.skuId = skuId;
    }

    public String getOrderId() {
        return orderId;
    }

    public void setOrderId(String orderId) {
        this.orderId = orderId;
    }

    public String getPurchaseToken() {
        return purchaseToken;
    }

    public void setPurchaseToken(String purchaseToken) {
        this.purchaseToken = purchaseToken;
    }

    public long getPurchaseTime() {
        return purchaseTime;
    }

    public void setPurchaseTime(long purchaseTime) {
        this.purchaseTime = purchaseTime;
    }

    public int getEntrance() {
        return entrance;
    }

    public void setEntrance(int entrance) {
        this.entrance = entrance;
    }

    public BillingOrder() {
    }

    public BillingOrder(String skuId, String orderId, String purchaseToken, long purchaseTime, int entrance) {
        this.skuId = skuId;
        this.orderId = orderId;
        this.purchaseToken = purchaseToken;
        this.purchaseTime = purchaseTime;
        this.entrance = entrance;
    }


    @Override
    public int describeContents() {
        return 0;
    }

    @Override
    public void writeToParcel(Parcel dest, int flags) {
        dest.writeString(this.skuId);
        dest.writeString(this.orderId);
        dest.writeString(this.purchaseToken);
        dest.writeLong(this.purchaseTime);
        dest.writeInt(this.entrance);
    }

    protected BillingOrder(Parcel in) {
        this.skuId = in.readString();
        this.orderId = in.readString();
        this.purchaseToken = in.readString();
        this.purchaseTime = in.readLong();
        this.entrance = in.readInt();
    }

    public static final Creator<BillingOrder> CREATOR = new Creator<BillingOrder>() {
        @Override
        public BillingOrder createFromParcel(Parcel source) {
            return new BillingOrder(source);
        }

        @Override
        public BillingOrder[] newArray(int size) {
            return new BillingOrder[size];
        }
    };
}
